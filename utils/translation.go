import "strings"

func Number2Ganan(n int) string {
	var bn = []string{"\u1040", "\u1041", "\u1042", "\u1043", "\u1044", "\u1045", "\u1046", "\u1047", "\u1048", "\u1049"}
	var r []string
	for n != 0 {
		r = append(r, bn[n%10])
		n /= 10
	}
	for i, j := 0, len(r)-1; i < j; i, j = i+1, j-1 {
		r[i], r[j] = r[j], r[i]
	}
	return strings.Join(r, "")
}

